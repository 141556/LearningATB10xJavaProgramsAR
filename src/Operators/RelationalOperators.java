package Operators;

public class RelationalOperators {
    public static void main(String[] args) {
        int num1 =50, num2 =100;
        System.out.println("num2 is grater than num1:"+(num2>num1));
        System.out.println("num1 is less than num2:"+(num1<num2));
        num2 =50;
        System.out.println("num1 and num2 are equal:"+(num1==num2));
        num1 =500;
        System.out.println("num2 is less than or equals to num1:"+(num2<=num1));
        System.out.println("num1 is greater than or equals to num2:"+(num1>=num2));
        System.out.println("num1 is not equals to num2:"+(num1!=num2));
    }

}
